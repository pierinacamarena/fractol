#include "../includes/libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*joinstr;
	int		len;

	if (!s1 || !s2)
		return (NULL);
	len = ft_strlen(s1) + ft_strlen(s2) + 1;
	joinstr = ft_strnew(len);
	if (!joinstr)
		return (NULL);
	ft_strcat(joinstr, s1);
	ft_strcat(joinstr, s2);
	return (joinstr);
}
