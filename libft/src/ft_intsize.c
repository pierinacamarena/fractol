#include "../includes/libft.h"

int		ft_intsize(int n)
{
	int		size;

	size = 0;
	if (n < 0)
	{
		size++;
		n = -n;
	}
	while (n != 0)
	{
		size++;
		n = n / 10;
	}
	return (size);
}
